package controlstatman.Loops;

public class switch1 {
    public static void main(String[] args) {
        int name = 3  ;
        switch(name){
            case 1:
            System.out.println("gautam");
            break;
            case 2:
            System.out.println("satrodhan");
            break;
            case 3:
            System.out.println("Arjun");
            break;
            case 4:
            System.out.println("pankaj");
            break;
        }
    }
}
